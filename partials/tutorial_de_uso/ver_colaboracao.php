        <p style="text-indent: 30px;">Ao realizar uma colaboração ela receberá o status "Em Avaliação", o(s) administrador(es) do sistema irá(ão) avalia-la e caso seu conteúdo não inflija nenhuma regra, seu status será alterado para "Aprovada", caso contrário, ela será reprovada e será omitida do sistema.</p>
